<?php

// Crear Constantes en PHP
// Las constantes en PHP se declaran con la función define() y no llevan el signo de pesos ($) al inicio del nombre de la constante.
define('PI', 3.141592653589793); // Definimos una constante llamada PI con el valor de 3.141592653589793

define('NOMBRE', 'Juan Triana'); // Definimos una constante llamada NOMBRE con el valor de 'Juan Triana'

echo 'La constante es: ' . NOMBRE . '<br>'; // Imprimimos el valor de la constante PI
?>
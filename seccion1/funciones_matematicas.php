<?php

$numero = 14.6;

// Redondear hacia arriba
echo "El número es: " . round($numero) . "<br>";

// Redondear hacia abajo
echo "El número es: " . floor($numero) . "<br>";

// Numero al azar de 1 a 10
echo "El número es: " . rand(1, 10) . "<br>";

// Rendondear hacia arriba con ceil
echo "El número es: " . ceil($numero) . "<br>";

// Constante PI con M_PI
echo "El número es: " . M_PI . "<br>";

?>